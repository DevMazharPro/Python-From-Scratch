#There are 5 data types in Python
# String, Floating Point Numbers, Integers, Booleans, None

a = 12
print(a, type(a)) # Integer

b = "Mazhar"
print(b, type(b)) # String

c = 2.5
print(c, type(c)) # Float 

d = True # or False
print(d, type(d)) # Boolean
 
e = None
print(e, type(e)) # None

