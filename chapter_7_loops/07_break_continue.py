# Break and Continue => 

# Break => it is used to exit the loop

for i in range(5):
    if i == 3:
        break # it will exit the loop when i == 3
    print(i)

# Continue => it is used to skip the current iteration

for i in range(5):
    if i == 3:
        continue # it will skip the current iteration when i == 3
    print(i)
