# List is the container of different values
fruits = ["Apple", "Banana", "Orange"]
print(fruits)
print(fruits[0])  # Apple
print(fruits[1])  # Banana
print(fruits[2])  # Orange

# List is mutable - we can change the values
fruits[0] = "Grape"
print(fruits)  # ['Grape', 'Banana', 'Orange']

# List can contain different data types
mixed_list = ["Apple", 1, True, 3.14]
print(mixed_list)  # ['Apple', 1, True, 3.14]

# List can contain lists
nested_list = [["Apple", "Banana"], ["Orange", "Grape"]]
print(nested_list)  # [['Apple', 'Banana'], ['Orange', 'Grape']]

